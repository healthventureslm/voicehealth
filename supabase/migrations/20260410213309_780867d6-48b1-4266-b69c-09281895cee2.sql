-- Security definer function to check roles
CREATE OR REPLACE FUNCTION public.has_role(_user_id UUID, _role app_role)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.user_roles WHERE user_id = _user_id AND role = _role
  )
$$;

-- Security definer function to get user department
CREATE OR REPLACE FUNCTION public.get_user_department(_user_id UUID)
RETURNS UUID
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT department_id FROM public.profiles WHERE user_id = _user_id
$$;

-- Updated at trigger function
CREATE OR REPLACE FUNCTION public.update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SET search_path = public;

-- Auto-create profile on signup
CREATE OR REPLACE FUNCTION public.handle_new_user()
RETURNS TRIGGER AS $$
BEGIN
  INSERT INTO public.profiles (user_id, full_name, avatar_url)
  VALUES (
    NEW.id,
    COALESCE(NEW.raw_user_meta_data->>'full_name', NEW.raw_user_meta_data->>'name', ''),
    COALESCE(NEW.raw_user_meta_data->>'avatar_url', NEW.raw_user_meta_data->>'picture', '')
  );
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER SET search_path = public;

CREATE TRIGGER on_auth_user_created
  AFTER INSERT ON auth.users
  FOR EACH ROW EXECUTE FUNCTION public.handle_new_user();

-- Updated at triggers
CREATE TRIGGER update_departments_updated_at BEFORE UPDATE ON public.departments FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();
CREATE TRIGGER update_profiles_updated_at BEFORE UPDATE ON public.profiles FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();
CREATE TRIGGER update_patients_updated_at BEFORE UPDATE ON public.patients FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();
CREATE TRIGGER update_consultations_updated_at BEFORE UPDATE ON public.consultations FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();
CREATE TRIGGER update_report_templates_updated_at BEFORE UPDATE ON public.report_templates FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();
CREATE TRIGGER update_clinical_protocols_updated_at BEFORE UPDATE ON public.clinical_protocols FOR EACH ROW EXECUTE FUNCTION public.update_updated_at_column();

-- RLS Policies

-- Departments: all authenticated users can read
CREATE POLICY "Authenticated users can view departments" ON public.departments FOR SELECT TO authenticated USING (true);
CREATE POLICY "Admins can manage departments" ON public.departments FOR ALL TO authenticated USING (public.has_role(auth.uid(), 'admin')) WITH CHECK (public.has_role(auth.uid(), 'admin'));

-- Profiles: users see own, admins see all
CREATE POLICY "Users can view own profile" ON public.profiles FOR SELECT TO authenticated USING (user_id = auth.uid());
CREATE POLICY "Admins can view all profiles" ON public.profiles FOR SELECT TO authenticated USING (public.has_role(auth.uid(), 'admin'));
CREATE POLICY "Users can update own profile" ON public.profiles FOR UPDATE TO authenticated USING (user_id = auth.uid()) WITH CHECK (user_id = auth.uid());
CREATE POLICY "System can insert profiles" ON public.profiles FOR INSERT WITH CHECK (true);

-- User roles: only admins manage, users view own
CREATE POLICY "Admins can manage roles" ON public.user_roles FOR ALL TO authenticated USING (public.has_role(auth.uid(), 'admin')) WITH CHECK (public.has_role(auth.uid(), 'admin'));
CREATE POLICY "Users can view own roles" ON public.user_roles FOR SELECT TO authenticated USING (user_id = auth.uid());

-- Patients: department-scoped
CREATE POLICY "Users can view patients in their department" ON public.patients FOR SELECT TO authenticated USING (department_id = public.get_user_department(auth.uid()));
CREATE POLICY "Users can insert patients in their department" ON public.patients FOR INSERT TO authenticated WITH CHECK (department_id = public.get_user_department(auth.uid()));
CREATE POLICY "Users can update patients in their department" ON public.patients FOR UPDATE TO authenticated USING (department_id = public.get_user_department(auth.uid()));
CREATE POLICY "Admins can manage all patients" ON public.patients FOR ALL TO authenticated USING (public.has_role(auth.uid(), 'admin')) WITH CHECK (public.has_role(auth.uid(), 'admin'));

-- Consultations: department-scoped
CREATE POLICY "Users can view consultations in their department" ON public.consultations FOR SELECT TO authenticated USING (department_id = public.get_user_department(auth.uid()));
CREATE POLICY "Users can insert consultations in their department" ON public.consultations FOR INSERT TO authenticated WITH CHECK (department_id = public.get_user_department(auth.uid()));
CREATE POLICY "Users can update own consultations" ON public.consultations FOR UPDATE TO authenticated USING (professional_id = auth.uid());
CREATE POLICY "Admins can manage all consultations" ON public.consultations FOR ALL TO authenticated USING (public.has_role(auth.uid(), 'admin')) WITH CHECK (public.has_role(auth.uid(), 'admin'));

-- Clinical reports: via consultation department
CREATE POLICY "Users can view reports in their department" ON public.clinical_reports FOR SELECT TO authenticated
  USING (EXISTS (SELECT 1 FROM public.consultations c WHERE c.id = consultation_id AND c.department_id = public.get_user_department(auth.uid())));
CREATE POLICY "Users can insert reports" ON public.clinical_reports FOR INSERT TO authenticated
  WITH CHECK (EXISTS (SELECT 1 FROM public.consultations c WHERE c.id = consultation_id AND c.department_id = public.get_user_department(auth.uid())));

-- Report templates: department-scoped or global
CREATE POLICY "Users can view active templates" ON public.report_templates FOR SELECT TO authenticated
  USING (is_active AND (department_id IS NULL OR department_id = public.get_user_department(auth.uid())));
CREATE POLICY "Admins can manage templates" ON public.report_templates FOR ALL TO authenticated
  USING (public.has_role(auth.uid(), 'admin')) WITH CHECK (public.has_role(auth.uid(), 'admin'));

-- Clinical protocols: department-scoped or global
CREATE POLICY "Users can view active protocols" ON public.clinical_protocols FOR SELECT TO authenticated
  USING (is_active AND (department_id IS NULL OR department_id = public.get_user_department(auth.uid())));
CREATE POLICY "Admins can manage protocols" ON public.clinical_protocols FOR ALL TO authenticated
  USING (public.has_role(auth.uid(), 'admin')) WITH CHECK (public.has_role(auth.uid(), 'admin'));

-- Clinical alerts: via consultation
CREATE POLICY "Users can view alerts for their consultations" ON public.clinical_alerts FOR SELECT TO authenticated
  USING (EXISTS (SELECT 1 FROM public.consultations c WHERE c.id = consultation_id AND c.department_id = public.get_user_department(auth.uid())));
CREATE POLICY "System can insert alerts" ON public.clinical_alerts FOR INSERT TO authenticated WITH CHECK (true);
CREATE POLICY "Users can dismiss alerts" ON public.clinical_alerts FOR UPDATE TO authenticated
  USING (EXISTS (SELECT 1 FROM public.consultations c WHERE c.id = consultation_id AND c.department_id = public.get_user_department(auth.uid())));

